let n = 5;
let remainder = n % 3;

switch (remainder) {
  case 0: // if remainder is 0
    console.log(n + " is divisible by 3");
    break;
  case 1: // if remainder is 1
    console.log(n + " is not divisible by 3");
    break;
  case 2: // if remainder is 2
    console.log(n + " is not divisible by 3");
}
